package com.example.domain;


public class CharWithPosition {
    private String c;
    private int x;
    private int y;

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
